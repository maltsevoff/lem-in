#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t		i;
	char		*res;

	i = 0;
	res = (char *)malloc(sizeof(char) * size + 1);
	if (!res)
		return (NULL);
	while (i < size)
	{
		res[i] = '\0';
		i++;
	}
	res[i] = '\0';
	return (res);
}
